def simmetr(s, i, j):
    """
    Рекурсивно определяет, является ли симметричной часть строки s
    с i-го по j-й символ включительно.

    Параметры:
    s - строка
    i - начальный индекс (включительно)
    j - конечный индекс (включительно)

    Возвращает:
    True - если часть строки симметрична (палиндром)
    False - в противном случае
    """
    i -= 1
    j -= 1

    if i >= j:
        return True

    if s[i] != s[j]:
        return False

    return simmetr(s, i + 2, j - 1)


s = input()
i = int(input())
j = int(input())
print(simmetr(s, i, j))
